const navToggle = document.querySelector(".nav-toggle");
const navLinks = document.querySelector(".nav-links");
const languageButtons = document.querySelectorAll("[data-lang-option]");
const metaDescription = document.querySelector("meta[name='description']");

const getValue = (source, path) =>
  path.split(".").reduce((value, key) => (value ? value[key] : undefined), source);

const applyLanguage = (language) => {
  const dictionary = translations[language] || translations.en;
  const pageTitle = language === "zh" ? document.body.dataset.pageTitleZh : document.body.dataset.pageTitleEn;

  document.documentElement.lang = language === "zh" ? "zh-Hans" : "en";
  document.title = pageTitle || dictionary.pageTitle;
  metaDescription?.setAttribute("content", dictionary.metaDescription);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getValue(dictionary, element.dataset.i18n);
    if (value) element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const value = getValue(dictionary, element.dataset.i18nPlaceholder);
    if (value) element.setAttribute("placeholder", value);
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.langOption === language;
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("tbc-language", language);
};

navToggle?.addEventListener("click", () => {
  const isOpen = navLinks.classList.toggle("is-open");
  navToggle.setAttribute("aria-expanded", String(isOpen));
});

navLinks?.addEventListener("click", (event) => {
  if (event.target instanceof HTMLAnchorElement) {
    navLinks.classList.remove("is-open");
    navToggle?.setAttribute("aria-expanded", "false");
  }
});

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.langOption || "en");
  });
});

document.querySelector(".contact-form")?.addEventListener("submit", (event) => {
  event.preventDefault();
  const button = event.currentTarget.querySelector("button[type='submit']");
  if (!button) return;

  const language = localStorage.getItem("tbc-language") || "en";
  button.textContent = translations[language]?.form.submitted || translations.en.form.submitted;
  button.setAttribute("disabled", "");

  window.setTimeout(() => {
    const currentLanguage = localStorage.getItem("tbc-language") || "en";
    button.textContent = translations[currentLanguage]?.form.submit || translations.en.form.submit;
    button.removeAttribute("disabled");
  }, 2200);
});

applyLanguage(localStorage.getItem("tbc-language") || "en");
